public class Bodega {
    private String codigo;
    private String nombre;
    private boolean estado;

    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @return the estado
     */
    public boolean isEstado() {
        return estado;
    }
    
    
    
}
